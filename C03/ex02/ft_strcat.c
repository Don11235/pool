char	*ft_strcat(char *dest, char *src)
{
	char	*pt;

	pt = dest;
	while (*pt)
		pt++;
	while (*src)
	{
		*pt = *src;
		pt++;
		src++;
	}
	*pt = '\0';
	return (dest);
}
