#include <unistd.h>

void	ft_putnbr(int nb)
{
	long int	n;
	int			i;
	char		num[12];

	i = 0;
	n = nb;
	if (n == 0)
	{
		write(1, "0", 1);
		return ;
	}
	if (n < 0)
	{
		n = n * -1;
		write(1, "-", 1);
	}
	while (n > 0)
	{
		num[i++] = 48 + (n % 10);
		n = n / 10;
	}
	while (i)
		write(1, &num[--i], 1);
}
