void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	sw;

	i = 0;
	while (i < size - 1)
	{
		j = i + 1;
		while (j < size)
		{
			if (*(tab + i) > *(tab + j))
			{
				sw = *(tab + i);
				*(tab + i) = *(tab + j);
				*(tab + j) = sw;
			}
			j++;
		}
		i++;
	}
}
